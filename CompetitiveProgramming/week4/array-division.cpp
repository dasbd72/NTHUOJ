using namespace std;
typedef long long ll;
void solve() {
    int N, K;
    vector<ll> vec;
    cin >> N >> K;
    for (int i = 0; i < N; i++) {
        ll x;
        cin >> x;
        vec.emplace_back(x);
    }
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    solve();
    return 0;
}
